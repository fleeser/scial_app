class SCIcon extends StatelessWidget {

  const SCIcon({
    super.key,
    required this.icon,
    required this.size,
    required this.color
  });

  final SCIcons icon;
  final double size;
  final Color color;
  
  @override
  Widget build(BuildContext context) {
    return SvgPicture.asset(
      icon.path,
      height: size,
      width: size,
      colorFilter: ColorFilter.mode(color, BlendMode.srcIn)
    );
  }
}