class SCFlag extends StatelessWidget {

  const SCFlag({
    super.key,
    required this.flag,
    this.width,
    this.height
  });

  final SCFlags flag;
  final double? width;
  final double? height;
  
  @override
  Widget build(BuildContext context) {
    return SvgPicture.asset(
      flag.path,
      height: height ?? width! * (3.0 / 4.0),
      width: width ?? height! * (4.0 / 3.0),
      package: 'scial_app_ui'
    );
  }
}